-- 1. products 테이블 생성 (상품 기본 정보 제공용임)
CREATE TABLE public.products (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 상품 고유 식별자임
  name text NOT NULL, -- 상품명임
  price bigint NOT NULL, -- 상품 가격임
  description text, -- 상품 설명임
  category text, -- 상품 카테고리임
  main_images text[] NOT NULL, -- 대표 이미지 URL 배열임
  is_visible boolean NOT NULL DEFAULT true, -- 상품 노출 여부 토글 컬럼임
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL -- 생성 일시임
);

-- 2. product_variants 테이블 생성 (색상 옵션 정보 제공용임)
CREATE TABLE public.product_variants (
  id text PRIMARY KEY, -- 옵션 고유 식별자임
  product_id bigint REFERENCES public.products(id) ON DELETE CASCADE NOT NULL, -- 참조 상품 ID임
  color_name text NOT NULL, -- 색상명임
  image text NOT NULL, -- 옵션 이미지 URL임
  is_visible boolean NOT NULL DEFAULT true -- 색상 노출 여부 토글 컬럼임
);

-- 3. customers 테이블 생성 (거래처 정보 제공용임)
CREATE TABLE public.customers (
  phone text PRIMARY KEY, -- 숫자만 파싱하여 저장하는 전화번호 기입란임
  shop_name text NOT NULL, -- 매장명임
  postcode text NOT NULL, -- 우편번호임
  address text NOT NULL, -- 주소임
  detail_address text NOT NULL, -- 상세 주소임
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL -- 생성 일시임
);

-- 4. orders 테이블 생성 (주문 정보 제공용임)
CREATE TABLE public.orders (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 주문 고유 식별자임
  customer_phone text REFERENCES public.customers(phone) ON DELETE RESTRICT NOT NULL, -- 고객 전화번호 외래키임
  delivery_method text NOT NULL, -- 배송 방법임
  payment_method text NOT NULL, -- 결제 방법임
  shop_delivery_info text, -- 매장 배송 요청 사항임
  notification_agreed boolean DEFAULT false NOT NULL, -- 알림 동의 여부임
  delivery_fee bigint DEFAULT 0 NOT NULL, -- 배송비임
  total_price bigint NOT NULL, -- 총 주문 금액임
  status text DEFAULT '주문 완료' NOT NULL, -- 주문 상태 정보임
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL -- 생성 일시임
);

-- 5. order_items 테이블 생성 (주문 상세 상품 정보 제공용임)
CREATE TABLE public.order_items (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 주문 아이템 고유 식별자임
  order_id bigint REFERENCES public.orders(id) ON DELETE CASCADE NOT NULL, -- 주문 ID 외래키임
  product_id bigint REFERENCES public.products(id) ON DELETE SET NULL, -- 상품 ID 외래키임
  product_name text NOT NULL, -- 상품명 스냅샷임
  variant_id text, -- 옵션 ID 스냅샷임
  variant_name text NOT NULL, -- 옵션명 스냅샷임
  image text NOT NULL, -- 옵션 이미지 URL 스냅샷임
  quantity bigint NOT NULL, -- 주문 수량임
  price bigint NOT NULL, -- 단가 스냅샷임
  item_status text DEFAULT '미포장' NOT NULL, -- 아이템별 진행 상태임
  status_updated_at timestamp with time zone, -- 상태 변경 시각 정보임
  is_checked boolean DEFAULT false NOT NULL -- 검증 체크 여부임
);

-- 6. misong_orders 테이블 생성 (미송 주문서 마스터 정보 제공용임)
CREATE TABLE public.misong_orders (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 미송 주문 고유 식별자임
  original_order_id bigint REFERENCES public.orders(id) ON DELETE SET NULL, -- 원본 주문 ID 외래키임
  customer_phone text REFERENCES public.customers(phone) ON DELETE RESTRICT NOT NULL, -- 고객 전화번호 외래키임
  delivery_method text NOT NULL, -- 배송 방법임
  payment_method text NOT NULL, -- 결제 방법임
  shop_delivery_info text, -- 매장 배송 정보임
  notification_agreed boolean DEFAULT false NOT NULL, -- 알림 동의 여부임
  delivery_fee bigint DEFAULT 0 NOT NULL, -- 배송비임
  total_price bigint NOT NULL, -- 총 미송 금액임
  status text DEFAULT '미송' NOT NULL, -- 미송 상태 정보임
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL -- 생성 일시임
);

-- 7. misong_order_items 테이블 생성 (미송 주문 상세 상품 정보 제공용임)
CREATE TABLE public.misong_order_items (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 미송 아이템 고유 식별자임
  misong_order_id bigint REFERENCES public.misong_orders(id) ON DELETE CASCADE NOT NULL, -- 미송 주문 ID 외래키임
  original_item_id bigint REFERENCES public.order_items(id) ON DELETE SET NULL, -- 원본 주문 아이템 ID 외래키임
  product_id bigint REFERENCES public.products(id) ON DELETE SET NULL, -- 상품 ID 외래키임
  product_name text NOT NULL, -- 상품명임
  variant_id text, -- 옵션 ID임
  variant_name text NOT NULL, -- 옵션명임
  image text NOT NULL, -- 옵션 이미지 URL임
  quantity bigint NOT NULL, -- 미송 수량임
  price bigint NOT NULL, -- 단가 정보임
  status text DEFAULT '미송' NOT NULL, -- 미송 상태 정보임
  status_updated_at timestamp with time zone, -- 상태 변경 시각 정보임
  is_checked boolean DEFAULT false NOT NULL -- 검증 체크 여부임
);

-- 8. admin_auth 테이블 생성 (어드민 비밀번호 검증용임)
CREATE TABLE public.admin_auth (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 어드민 인증 식별자임
  password_hash text NOT NULL, -- 비밀번호 해시값임
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL -- 생성 일시임
);

-- 초기 어드민 데이터 주입 가동함 (postgresql 내장 pgcrypto를 통해 bcrypt 암호화하여 주입, 비밀번호는 '012560'임)
INSERT INTO public.admin_auth (password_hash) VALUES (extensions.crypt('012560', extensions.gen_salt('bf', 10)));

