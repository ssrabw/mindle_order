-- Admin Auth 보안 정책 정보임 (SELECT 차단 — 프론트엔드에서 직접 조회 불가능함)
-- admin_auth 테이블은 RPC 함수를 통해서만 서버사이드에서 접근 가능함

-- 12. 비밀번호 검증 RPC 함수 (SECURITY DEFINER로 RLS 우회하며, pgcrypto의 crypt 함수를 사용해 bcrypt 해시 검증)
-- 실행 전에 Supabase 대시보드에서 `CREATE EXTENSION IF NOT EXISTS pgcrypto;`가 실행되어 있어야 합니다.
CREATE OR REPLACE FUNCTION public.verify_admin_password(input_password text)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.admin_auth 
    WHERE crypt(input_password, password_hash) = password_hash
  );
END;
$$;

